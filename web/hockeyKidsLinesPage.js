function buildMatchView(data) {
    let result = ["<table border=1'>"];
    let i = 0;
    for(let row of data) {
        result.push(`<tr><td><th>Player ${++i}</th></td>`);
        for(let cell of row.split("").reverse().join("")){
            result.push(`<td>${cell == '0' ? ' ': i}</td>`);
        }
        result.push("</tr>");
    }
    result.push("</table><br>");
    return result.join('\n');
}